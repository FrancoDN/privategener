                <h2 class="text-center md:text-3xl text-xl text-gray-400 mt-4 mb-8">Welcome PV</h2>
                
                <div class="block my-4">
                    <label for="discord" class="block text-gray-400 text-sm">Discord</label>
                    <input id="discord" v-model="discord" type="text" class="w-full h-10 py-2 px-2 border-2 border-gray-300 rounded-md">
                
                </div>

                <div class="block my-4">
                    <label for="user" class="block text-gray-400 text-sm">User</label>
                    <input id="user" v-model="user" type="text" class="w-full h-10 py-2 px-2 border-2 border-gray-300 rounded-md">
                
                </div>
                
                <div class="block my-4">
                    <label for="password" class="block text-gray-400 text-sm">Password</label>
                    <input id="password" v-model="password" type="password" class="w-full h-10 py-2 px-2 border-2 border-gray-300 rounded-md">
                </div>

                <button class="bg-blue-500 h-10 text-gray-100 py-2 px-4 w-full rounded-md bold" @click="registrar()">Register</button>
                
            </div>
        </div>

        <footer class="text-center">
