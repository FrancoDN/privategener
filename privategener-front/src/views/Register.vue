                <span class="text-gray-600 text-xs text-center">ChadGeners</span>
            </div>
        </footer>
    </div>
</template>

<script>
import firebase from 'firebase'
import donorServices from '../services/donor.services.js'
export default {
    data() {
        return {
            discord: "",
            user: "",
            password: "",
        };
    },
    name: "Basic",

    methods: {
        registrar() {
      if (this.user && this.password) {
        firebase.auth().createUserWithEmailAndPassword(this.user, this.password)
        .then(() => {
            let donor = {
              discord: this.discord,
              uid: firebase.auth().currentUser.uid,
              active: true,
              license: 1
            };
            localStorage.setItem('user', donor.uid);
            donorServices.postDonor(donor);
            this.$router.push('/');
          })
          .catch((err) => {
              console.log(err);
          });
      }
    },
    },
};
</script>

